<h3>Free intermediate target certification</h3>
<p>If you are currently a beginner member of our club, and want to advance to the
    farther targets, you can! Just ask one of the rangemasters for what we call 
    "the 10m test". <br />
    <br />
    <a href="cert.php">Rules and info</a>.</p>
<hr />

<h3>Tournaments</h3>
<p>We host and attend to tournaments! The University of Waterloo often competes with
   regional "big shots", such as the University of Toronto, York, and Queen's. We usually
   return with prizes and trophies.
</p>

