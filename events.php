<h3>Public fun shoot days</h3>
<p>Occuring once or twice every term, archers have the option to, under close supervision,
aim at all ready targets, including bullseyes, soft foam, and wood.</p>
<hr />

<h3>Photography fun shoot</h3>
<p>On some terms, a photographer will appear on the range and shoot us with a camera.
Feel free to pose or dodge as required.</p>
