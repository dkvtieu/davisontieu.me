<p><b><a name="Fees"></a>How much to join?</b>
    <br/>Archery term membership: &dollar;20 (&dollar;15 if joining after our midterm date,
    different every year.)
    <br/>Also includes storage fees.
    <br/>* we do not accept &dollar;100 bills or loose change.
    <br/>
    <br/>You can also enter without a membership for &dollar;5 per day (these can only
    be obtained a maximum of three times in one term).
    These guest passes are not available to those who are beginners requiring
    full Instruction.
    Due to liability, a guest is defined as University of Waterloo member or an
    experienced archer if s/he is a non-member of University of Waterloo.
    Any non-University of Waterloo members who wish to participate in the UWAC
    must obtain an approval from the PAC manager.
    The Executives would request the approval on his/her behalf.
    <br/>
    <br/>For non-uwaterloo students,
    <br/>PAC term fee: <a href="http://clubs.uwaterloo.ca/clubs.php?club=10">&dollar;50-135</a>
    <br/>PAC day fee: <a
        href="http://clubs.uwaterloo.ca/clubs.php?club=10">&dollar;5</a>
</p>
<p><b><a name="Hours">When is the club open?</a>
    </b>
    <br/>Exact hours can be found at <a
        href="http://clubs.uwaterloo.ca/clubs.php?club=10">the campusrec
        website</a>.
    <br/>Shooting stops at the last 10-20 minutes of the hours of operation.
    <br/>The club may also be closed due to bad weather and insufficient
    staffing.
    <br/>
    <br/> <b><a name="Dangerous"></a>Is archery dangerous?</b>
    <br/>Hardly. However, any sport is dangerous if not done properly. It is
    important to observe
    line calls, to be patient, and to treat others and the equipment with
    respect. Participants are required to pay for any damage in equipment that
    is cause by his/her carless behaviour, e.g., 'dry-fire' the bow.
    <br/>
    <br/>Also, according to the club constitution, &quot;The Executive has the
    right to refuse/terminate membership to anyone at anytime whom it believes
    within reasonable grounds is a hazard to themselves, others, or club
    property.&quot;
</p>
