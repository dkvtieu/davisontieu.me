<p><b><a name="Bring"></a>What do I need to bring when I show up
        to the first session?</b>
    <br/>Your WatCard, <a href="#Fees">the fees</a>, and maybe a friend.
    Comfortable, non-baggy clothing (short sleeve shirt, or
    shirt with tight sleeves), and something to tie your hair
    back. If a hijab (headscarf equivalents) is worn, the ends must be secured
    to avoid interference with the bow.
    <br/>
    <br/>Do not wear things that can get caught: scarves, dangly
    earrings, a button-up shirt, or facial jewelry. Do not wear
    uncomfortable or movement restricting items, like
    stilettos, a corset, etc.
</p>
<p><b><a name="NeedEquip" id="NeedEquip"></a>Do I need my own
        equipment?</b>
    <br/>No, the club provides everything you need.
</p>
<p><b><a name="BringEquip" id="BringEquip"></a>Can I bring my own
        equipment?</b>
    <br/>Yes, as long as it is less than 55lb (in draw weight) and in safe
    working order, subject to club review.
    <br/>If using the club's arrows, the draw weight must be less than or equal
    to 40lb.
    <br/>Note: crossbows and other firearms are prohibited.
</p>
<p><b><a name="StoreEquip"></a>Can I store my equipment
        there?</b>
    <br/>Yes, subject to availability. It is stored at your
    own risk, and must be removed by the end of the term.
    Please speak to a club executive if you are interested.
</p>
<p><b><a name="Attendence"></a>Do I have to attend all
        nights?</b>
    <br/>No. After the first lesson (the safety lesson), you can come whenever
    you
    want. However, there are some reserved times for
    intermediate / advanced archers, as well as lessons /
    workshops for beginners.
</p>
<p><b><a name="Cap"></a>How many people are in the club?</b>
    <br/>Fall / Winter: 50-60, Summer: 20-30
</p>
<p><b><a name="CommonProblems"></a>What are common
        problems?</b>
    <br/>For first time archers: correct clothing, familiarity
    with equipment, and group safety are usually solved
    quickly. Intermediate archers will have trouble with poor
    form, equipment configuration "tuning", and athletic
    preparedness. Advanced archers typically have trouble with
    sport psychology, muscle strain due to heavier equipment,
    and small but signifigant form and equipment problems.
</p>
<p><b><a name="NotAStudent"></a>I am not a student. Can I join?</b>
    <br/>University of Waterloo Faculty, alumni, staff, and students can join.
    You must be a member of the PAC, and have paid all applicable fees.
    Otherwise, you cannot join as you are not properly insured.
</p>
<p><b><a name="Visiting"></a>I am visiting Waterloo. Can I use the facilities?</b>
    <br/>You must be a member of University of Waterloo. Subject to
    availability, you can: you must pay both the PAC day fee and the archery
    day fee.
</p>
