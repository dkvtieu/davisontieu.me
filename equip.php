<h1>Equipment</h1>

<p><b>Do I need to buy my own equipment?</b><br>
    No. <br>
    The University of Waterloo Archery Club has a good selection of beginner and
    intermediate level equipment for club use. </p>

<p><b>Do I need to pay to borrow the club's equipment?</b><br>
    If you are a member, your membership fee covers the use of our equipment
    on the range. <br>
    <br>
    If you wish to borrow our equipment for personal use, a day-rate applies.</p>
<p><b>What kinds of equipment does the club have?</b><br>
    The club focuses on Olympic style shooting primarily and stocks only recurve
    bows. The executive members have a good foundation of knowledge to instruct
    and inform members who wish to shoot in other classes, but the archer would
    be required to provide his or her own equipment. <br>
    <br>
    As for accessories beginners have access to finger tabs, arm guards, low
    quality sights and chest guards (if needed). Intermediate archers may also
    have access to better finger tabs, better sights, stabilizers, clickers,
    better rests, plungers, hip quivers as well as better arrows. (Please note
    that intermediate accessories will be primarily reserved for those
    attending tournaments, merit and seniority will also be factored in.)
    </p>
<p><b>Which levels of equipment does the club buy?</b><br>
    With the exception of some older stock, the beginner level bows tend to be
    Victorys. Intermediate level bows tend to be from the Win and Win Evolution
    Series. <br>
    <br>
    Our intermediate level bows are few, and are typically given out on the
    basis of merit, tournament attendance and seniority.</p>

<p>If you have any questions regarding the clubs equipment, purchasing
    equipment or equipment in general, please feel free to talk to <a
        href="exec.php">our executives</a>.</p>